            .onSnapshot((res) => {
                this.getMessages()
            });
    },
    data() {
        return {
            message: null,
            listMessages: [],
            sendID: 1,
            sendName: "Truong",
        }
    },
    created() {
        this.getMessages()
    },
    methods: {
        sendMessage() {
            db.collection("conversations").add({
                message: this.message,
                send_id: this.sendID,
                send_name: this.sendName,
                created_at: new Date(),
            }).then((docRef)=>{
                this.message = null
                this.getMessages()
            }).catch((error)=>{
                console.log("Error adding document: ", error);
            });
        },
        getMessages() {
            db.collection("conversations").get()
                .then((querySnapshot)=>{
                    const messages = []
                    querySnapshot.forEach((doc)=>{
                        console.log(`${doc.id} => ${doc.data().sendName}`)
                        messages.push(doc.data())
                    });
                    this.listMessages = messages
                })
        }
    }
}
